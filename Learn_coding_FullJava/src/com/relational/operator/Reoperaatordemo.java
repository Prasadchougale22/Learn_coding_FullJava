package com.relational.operator;

public class Reoperaatordemo {

	public static void main(String[] args) {
		int a = 10, b = 20;
		System.out.println(a == b);// 10==20 false
		System.out.println(a != b);// 10!=20 true
		System.out.println(a < b);// 10<20true
		System.out.println(a > b);// 10>20false
		System.out.println(a >= b);// 10>=20false
		System.out.println(a <= b);// 10<=20true

	}

}
